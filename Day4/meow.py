# while loop – Best for unknown repetitions
# for loop – Best for fixed repetitions
#cat meoww! 
print("using while loop")
i=0   
while i<=2 :  # count starts from 0 
    print("Meoww!")  
    i=i+1

# for loop
print("using for loop")
for i in  [0,1,2] : #list
    print("Meoww")  
print("_")
for _ in range(3):  #the count goes until the number but not through the number specified
    print("Meowww")
   
print("_")   
print("meow\n" * 3, end="")
